XBHELP_CALLOUT g_HelpCallouts[] = 
{
    { XBHELP_BACK_BUTTON,  XBHELP_PLACEMENT_2, L"Display help" },
    { XBHELP_A_BUTTON,     XBHELP_PLACEMENT_2, L"Pause" },
};

#define NUM_HELP_CALLOUTS 2


//-----------------------------------------------------------------------------
// Name: class CXBoxSample
// Desc: Application class.
